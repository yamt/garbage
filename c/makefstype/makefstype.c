#include <stdint.h>
#include <stdio.h>

/* copy-and-paste from netbsd vfs_mount.c */
long
makefstype(const char *type)
{
        long rv;

        for (rv = 0; *type; type++) {
                rv <<= 2;
                rv ^= *type;
        }
        return rv;
}

/*
 * ./a.out ffs nfs mfs msdos lfs fdesc null overlay umap kernfs procfs afs
 * cd9660 union adosfs ext2fs coda filecore ntfs smbfs ptyfs tmpfs udf sysvbfs
 * puffs hfs efs zfs nilfs rumpfs v7fs autofs|sort -k3
 */

int
main(int argc, void **argv)
{
        int i;
        for (i = 1; i < argc; i++) {
                const char *type = argv[i];
                printf("%10s %08lx %02x\n", type, makefstype(type),
                       (uint8_t)makefstype(type));
        }
}
